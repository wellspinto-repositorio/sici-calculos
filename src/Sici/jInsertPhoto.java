    private void btProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarActionPerformed
        String nimg = selecionarDiretorio("fotos");
        if (nimg != null) {
            nmImagem.setText(nimg);
            preview.setIcon(new ResizeImageIcon("E", nimg, preview.getWidth(), preview.getHeight()).getImg());
        }
    }//GEN-LAST:event_btProcurarActionPerformed

    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        BufferedImage imagem = BlobFields.ImageIcon2BufferedImage((ImageIcon) preview.getIcon());
        try {
            ImageIO.write(imagem, "JPG", new File("fotos/" + this.imgname + ".jpg"));
        } catch (Exception e) {e.printStackTrace();}
        this.dispose();
    }//GEN-LAST:event_btGravarActionPerformed

    public String selecionarDiretorio(String pastainicial) {
      String caminho = null;  
  
      JFileChooser dFC = new JFileChooser(pastainicial);
      dFC.setFileSelectionMode(JFileChooser.FILES_ONLY);  
  
      int resposta = dFC.showOpenDialog(this);  
      if (resposta == JFileChooser.APPROVE_OPTION) {  
         caminho = dFC.getSelectedFile().getPath();  
      } else {  
         System.out.print("Execução cancelada.");  
      }  
  
      return caminho;  
   }  
    
