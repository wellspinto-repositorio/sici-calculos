    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        if (sNome.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo 'NOME' nÃ o pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            sNome.requestFocus();
            return;
        }

        if (sCpf.getText().replace(".", "").replace("-", "").trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Campo 'CPF' nÃ o pode ser vazio!!!", "Erro", JOptionPane.ERROR_MESSAGE);
            sCpf.requestFocus();
            return;
        }

        try {
            GravarDados();
        } catch (SQLException ex) { ex.printStackTrace();}

        bNew = false;

        // Bloqueio dos botÃµes
        btIncluir.setEnabled(!(VariaveisGlobais.mQtdSoc > 3));
        btGravar.setEnabled(true);
        btRetornar.setEnabled(true);

    }//GEN-LAST:event_btGravarActionPerformed

    private void btRetornarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRetornarActionPerformed
        if (bNew) {
            try {
                LerDados();

                // Bloqueio dos botÃµes
                btIncluir.setEnabled(!(VariaveisGlobais.mQtdSoc > 3));
                btGravar.setEnabled(true);
                btRetornar.setEnabled(true);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
            bNew = false;
        } else this.dispose();
    }//GEN-LAST:event_btRetornarActionPerformed

