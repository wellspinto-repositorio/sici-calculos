    private void LerMensagens() throws SQLException {
        try {
            jCab1.setText(conn.ReadParameters("MSGCABBOL1"));
            jCab2.setText(conn.ReadParameters("MSGCABBOL2"));
            jCabDoc.setText(conn.ReadParameters("MSGCABBOLDOC"));

            jLinha1.setText(conn.ReadParameters("MSGBOL1"));
            jLinha2.setText(conn.ReadParameters("MSGBOL2"));
            jLinha3.setText(conn.ReadParameters("MSGBOL3"));
            jLinha4.setText(conn.ReadParameters("MSGBOL4"));
            jLinha5.setText(conn.ReadParameters("MSGBOL5"));
            jLinha6.setText(conn.ReadParameters("MSGBOL6"));
            jLinha7.setText(conn.ReadParameters("MSGBOL7"));
            jLinha8.setText(conn.ReadParameters("MSGBOL8"));
            jLinha9.setText(conn.ReadParameters("MSGBOL9"));

            jLinha10.setText(conn.ReadParameters("MSGBOL10"));
            jLinhaMsgAniversário.setText(conn.ReadParameters("MSGANIVERSARIO"));

            jcbAniversario.setSelected(("TRUE".equals(conn.ReadParameters("ANIVERSARIO").toUpperCase()) ? true : false));
            jcbComemorativas.setSelected(("TRUE".equals(conn.ReadParameters("FERIADOS").toUpperCase()) ? true : false));
        } catch (Exception ex) {}
    }

    private void GravarMensagens() throws SQLException {
        conn.SaveParameters(new String[] {"MSGCABBOL1", jCab1.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGCABBOL2", jCab2.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGCABBOLDOC", jCabDoc.getText(), "TEXTO"});

        conn.SaveParameters(new String[] {"MSGBOL1", jLinha1.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL2", jLinha2.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL3", jLinha3.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL4", jLinha4.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL5", jLinha5.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL6", jLinha6.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL7", jLinha7.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL8", jLinha8.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL9", jLinha9.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGBOL10", jLinha10.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"MSGANIVERSARIO", jLinhaMsgAniversário.getText(), "TEXTO"});
        conn.SaveParameters(new String[] {"ANIVERSARIO", (jcbAniversario.isSelected() ? "TRUE" : "FALSE"), "LOGICO"});
        conn.SaveParameters(new String[] {"FERIADOS", (jcbComemorativas.isSelected() ? "TRUE" : "FALSE"), "LOGICO"});
    }    

