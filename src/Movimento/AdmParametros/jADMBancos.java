    private void pixExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pixExcluirActionPerformed
        Object[] options = { "Sim", "Não" };
        int n = JOptionPane.showOptionDialog(null,
            "Deseja este conta PIX ? ",
            "Atenção", JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        if (n == JOptionPane.NO_OPTION) return;

        int pos = pixTable.getSelectedRow();
        if (pos > -1) {
            String tid = pixTable.getModel().getValueAt(pos, 0).toString().trim();
            String sql = "DELETE FROM bancos_pix WHERE id = '" + tid + "';";
            try { conn.CommandExecute(sql); } catch (Exception e) {e.printStackTrace();}
        }
        LerBancoPix();
    }//GEN-LAST:event_pixExcluirActionPerformed

