    private void pixTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pixTipoActionPerformed
        int selecao = pixTipo.getSelectedIndex();
        pixChave.setValue(null);
        if (selecao == 0) {
            try { pixChave.setFormatterFactory(new DefaultFormatterFactory(new MaskFormatter("+##(##)#####-####"))); } catch (ParseException ex) {}
        } else if (selecao == 1 || selecao > 3) {
            try { pixChave.setFormatterFactory(new DefaultFormatterFactory(null)); } catch (Exception ex) {}
        } else if (selecao == 2) {
            try { pixChave.setFormatterFactory(new DefaultFormatterFactory(new MaskFormatter("###.###.###-##"))); } catch (ParseException ex) {}
        } else if (selecao == 3) {
            try { pixChave.setFormatterFactory(new DefaultFormatterFactory(new MaskFormatter("##.###.###/####-##"))); } catch (ParseException ex) {}
        }
        pixChave.requestFocus();
    }//GEN-LAST:event_pixTipoActionPerformed

    private void pixBancoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pixBancoFocusLost
        if (pixInclusao) {
            // Gravar
            String insertSQL = "INSERT INTO `bancos_pix`(`tipo`,`chave`,`banco`) " +
            "VALUES ('" + pixTipo.getSelectedItem().toString() + "','" +
            pixChave.getText() + "','" + pixBanco.getText() + "');";
            conn.CommandExecute(insertSQL);

            pixTipo.setEnabled(false);
            pixChave.setEnabled(false);
            pixBanco.setEnabled(false);

            pixInclusao = false;
            LerBancoPix();
        }
    }//GEN-LAST:event_pixBancoFocusLost

