    private void seekButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seekButtonActionPerformed
        seekButton.setEnabled(false);
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        
        String nomeArq = FuncoesGlobais.escolherArquivo("Selecione o arquivo de retorno", VariaveisGlobais.RETORNO_PATH,"Arquivos de retorno bancário", "ret", "txt");
        if (nomeArq == null) {
            JOptionPane.showMessageDialog(this, "Sem arquivo de retorno selecionado.");
            
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            seekButton.setEnabled(true);            
            return;
        }
        
        fileName.setText(nomeArq);
        
        // Pega no ret o codigo do banco
        String bankNumber = new Bancos.bancos(VariaveisGlobais.conexao).loadReturnFile(nomeArq)[0].substring(0,3);
        
        Class codLiquidacao = null; Object callInvokeClass = null;
        if (bankNumber.equalsIgnoreCase("033")) {
            try { 
                codLiquidacao = Class.forName("Bancos.codLiquSantander"); 
            } catch (ClassNotFoundException | LinkageError cnfEx) { 
                cnfEx.printStackTrace(); 
            }            
            try { callInvokeClass = codLiquidacao.newInstance(); } catch (InstantiationException | IllegalAccessException iiaEx) {}
        } else if (bankNumber.equalsIgnoreCase("341")) {
            try { 
                codLiquidacao = Class.forName("Bancos.codLiquItau"); 
            } catch (ClassNotFoundException | LinkageError cnfEx) { 
                cnfEx.printStackTrace(); 
            }            
            try { callInvokeClass = codLiquidacao.newInstance(); } catch (InstantiationException | IllegalAccessException iiaEx) {}
        } else if (bankNumber.equalsIgnoreCase("104")) {
            // 
        } else {
            JOptionPane.showMessageDialog(this, "Este não é um arquivo de retorno válido!");
            
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            seekButton.setEnabled(true);            
            return;
        }
        
        // Processa o retorno
        cRetorno returnFile = new Bancos.bancos(VariaveisGlobais.conexao).retorno(nomeArq, bankNumber);
        if (returnFile == null) {
            JOptionPane.showMessageDialog(this, "O arquivo de retorno esta vazio!");
            
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            seekButton.setEnabled(true);            
            return;
        }
        
        // Pegar dados do banco
        Object[][] banckDados = null;
        try { 
            banckDados = db.ReadFieldsTable(new String[] {"codigo", "nome"}, "bancos", "codigo = :codigo", new Object[][] {{"string", "codigo", bankNumber}}); 
        } catch (SQLException sqlEx) {}
        
        if (banckDados == null) {
            JOptionPane.showMessageDialog(this, "Banco não cadastrado no sistema!");
            
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            seekButton.setEnabled(true);            
            return;
        }
        
        bankName.setText(banckDados[0][3].toString() + " - " + banckDados[1][3].toString());
        fileDate.setText(new Bancos.bancos(VariaveisGlobais.conexao).fmtData(returnFile.getDatacredito()));
        
        conLista.setAutoResizeMode(AUTO_RESIZE_OFF);
        TableControl.header(conLista, new String[][] {
            {"Vencimento","Pagamento","SeuNumero","NossoNumero","CnpjCpf","Sacado","Multa","Valor","Boleta","Situação"},
            {"80","80","80","80","0","240","70","80","80","500"}
        });
        TableControl.Clear(conLista);  
        
        int qtdBaixar = 0; BigDecimal vrBaixar = new BigDecimal("0");
        for (int pos = 0; pos <= returnFile.getSegmentot().size() - 1; pos++) {
            cSegmentoT segmentoT = returnFile.getSegmentot().get(pos);
            cSegmentoU segmentoU = returnFile.getSegmentou().get(pos);
            
            // Codigo de Ocorrência
            String descOcor = ""; String subOcor = "";
            if (bankNumber.equalsIgnoreCase("033")) {
                Method mthd = null;
                try { mthd = callInvokeClass.getClass().getMethod("getOcorrencias", new Class[] {String.class}); } catch (NoSuchMethodException | SecurityException nmfEx) {}
                try { descOcor = mthd.invoke(callInvokeClass, segmentoU.getCodocor()).toString(); } catch (InvocationTargetException | IllegalArgumentException | IllegalAccessException itEx) {}
                
                //try { mthd = callInvokeClass.getClass().getMethod("getLiquidacao", new Class[] {String.class}); } catch (NoSuchMethodException | SecurityException nmfEx) {}
                //if (!segmentoT.getCodliquidacao().trim().isEmpty()) {
                //    try { subOcor = mthd.invoke(callInvokeClass, segmentoT.getCodliquidacao()).toString(); } catch (InvocationTargetException | IllegalArgumentException | IllegalAccessException itEx) {}
                //}                                
                
                //if (!segmentoT.getCodliquidacao().trim().isEmpty()) descOcor += " / " + subOcor;
            } else if (bankNumber.equalsIgnoreCase("341")) {
                Method mthd = null;
                try { mthd = callInvokeClass.getClass().getMethod("getOcorrencias", new Class[] {String.class}); } catch (NoSuchMethodException | SecurityException nmfEx) {}
                try { descOcor = mthd.invoke(callInvokeClass, segmentoU.getCodocor()).toString(); } catch (InvocationTargetException | IllegalArgumentException | IllegalAccessException itEx) {}
                
                try { mthd = callInvokeClass.getClass().getMethod("getLiquidacao", new Class[] {String.class}); } catch (NoSuchMethodException | SecurityException nmfEx) {}
                if (!segmentoT.getCodliquidacao().trim().isEmpty()) {
                    try { subOcor = mthd.invoke(callInvokeClass, segmentoT.getCodliquidacao()).toString(); } catch (InvocationTargetException | IllegalArgumentException | IllegalAccessException itEx) {}
                }                                
                
                if (!segmentoT.getCodliquidacao().trim().isEmpty()) descOcor += " / " + subOcor;
            } else if (bankNumber.equalsIgnoreCase("104")) {
                //
            }

            // Quando tiver um vencimento em branco CEF
            if (segmentoT.getVencimento().equalsIgnoreCase("00000000")) continue;
            
            TableControl.add(conLista, new String[][] {
                {
                    new Bancos.bancos(VariaveisGlobais.conexao).fmtData(segmentoT.getVencimento()),
                    segmentoU.getDataocor().equalsIgnoreCase("00000000") ? "" : new Bancos.bancos(VariaveisGlobais.conexao).fmtData(segmentoU.getDataocor()),
                    new Bancos.bancos(VariaveisGlobais.conexao).fmtSeuNumero(segmentoT.getSeunumero().trim()),
                    new Bancos.bancos(VariaveisGlobais.conexao).fmtNossoNumero(segmentoT.getNnumero(), segmentoT.getDacnnumero()),
                    segmentoT.getInscpagador(),
                    segmentoT.getNomepagador().trim(),
                    segmentoU.getJurousmulta().equalsIgnoreCase("000000000000000") ? "0,00" : new Bancos.bancos(VariaveisGlobais.conexao).fmtValue(segmentoU.getJurousmulta()),
                    segmentoU.getValorlanc().equalsIgnoreCase("000000000000000") ? "0,00" : new Bancos.bancos(VariaveisGlobais.conexao).fmtValue(segmentoU.getValorlanc()),
                    segmentoT.getVrtitulo().equalsIgnoreCase("000000000000000") ? "0,00" : new Bancos.bancos(VariaveisGlobais.conexao).fmtValue(segmentoT.getVrtitulo()),
                    descOcor
                },
                {"C","C","C","C","L","L","R","R","R","L"},
            }, true);
            
            if (LerValor.StringToFloat(new Bancos.bancos(VariaveisGlobais.conexao).fmtValue(segmentoU.getValorlanc())) != 0 && LerValor.StringToFloat(new Bancos.bancos(VariaveisGlobais.conexao).fmtValue(segmentoT.getVrtitulo())) != 0 ) {
                qtdBaixar++;
                vrBaixar = vrBaixar.add(new BigDecimal(new Bancos.bancos(VariaveisGlobais.conexao).fmtValue(segmentoU.getValorlanc()).replace(".", "").replace(",", ".")));
            }
        }
        
        quantityBoleta.setText(FuncoesGlobais.StrZero(String.valueOf(qtdBaixar), 4));
        valueBoleta.setText(LerValor.floatToCurrency(vrBaixar.floatValue(), 2));
        
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        seekButton.setEnabled(true);                    
    }//GEN-LAST:event_seekButtonActionPerformed


